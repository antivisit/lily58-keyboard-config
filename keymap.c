bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) { 
    case MO(_NAVI):
      if (!record->event.pressed) {
        // Al soltar MO(_NAVI), verificar si alt_tab_active está activo
        if (alt_tab_active) {
          // Desactivar alt_tab_active y liberar Alt solo si está activo
          alt_tab_active = false;
          unregister_code(KC_LALT);
        }
      }
      break;

    case FN_TAB:
      if (record->event.pressed) {
        // Al presionar FN_TAB por primera vez, activar alt_tab_active y enviar Alt + Tab
        if (!alt_tab_active) {
          alt_tab_active = true;
          register_code(KC_LALT);
          register_code(KC_TAB);
        }
        // Si alt_tab_active ya está activo, enviar Tab
        else if (alt_tab_active) {
          register_code(KC_TAB);
        }
      } else {
        // Liberar Tab al soltar FN_TAB
        unregister_code(KC_TAB);
      }
      break;
  }
  return true;
}